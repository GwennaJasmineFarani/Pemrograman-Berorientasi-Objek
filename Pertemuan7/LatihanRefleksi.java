package Pertemuan7;

public class LatihanRefleksi {
    public static void main(String[] args) {

        System.out.println(" === MENGGUNAKAN WHILE ===");
        int a = 1; 
        while (a <= 5) {  // menggunakan while
        System.out.print(a + " ");
        a++; // Update
        }

        System.out.println(); 

        System.out.println(" === MENGGUNAKAN DO-WHILE LOOP ===");
        int b = 1; // Inisialisasi
        do {
        System.out.print(b + " ");
            b++; // Update
        }   
        while (b <= 5); 

        System.out.println(); 

        System.out.println(" === MENGGUNAKAN FOR === "); 
        for (int c = 1; c <= 5; c++) {
        
        System.out.print(c + " ");
        }
    }
}

